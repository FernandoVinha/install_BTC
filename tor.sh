# Atualiza a lista de pacotes
sudo apt update
# Este comando utiliza o apt para atualizar a lista de pacotes disponíveis no sistema.

# Instala o Tor com a opção '-y' para confirmar automaticamente a instalação
sudo apt install -y tor
# Este comando utiliza o apt para instalar o Tor. A opção '-y' indica que a instalação deve prosseguir sem pedir confirmação do usuário.

# Inicia o serviço Tor
sudo service tor start
# Este comando inicia o serviço Tor utilizando o sistema de inicialização do sistema operacional. O serviço Tor agora está em execução.

